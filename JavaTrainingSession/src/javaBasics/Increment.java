package javaBasics;

public class Increment {
	
	public static void main(String[] args) {
		
		int a =3;
		int b = ++a; //pre increment
		System.out.print(a);
		System.out.print(b);
		
		int a1 =3;
		int b1 = a1++; //post increment
		System.out.print(a1);
		System.out.print(b1);
		
		int a2 =3;
		int b2 = --a2; //pre decrement
		System.out.print(a2);
		System.out.print(b2);
		

		int a3 =3;
		int b3 = a3--; //post decrement
		System.out.println(a3);
		System.out.println(b3);
		
		
	}

}

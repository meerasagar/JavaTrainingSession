package encapsulation;

public class Example1 {

	private int age;
	private int EmpID;
	private String name;

	public void setAge(int age) {

		this.age = age;

	}

	public int getAge() {
		return age;
	}

	public void setEmpID(int EmpID) {
		this.EmpID = EmpID;
	}

	public int getEmpID() {
		return EmpID;
	}

	public void setName(String name) {

		this.name = name;

	}

	public String getName() {
		return name;
	}

}
